SELECT `販CO`, 
`販売先名`, 
`商品名`, 
`原価`, 
`販売数`,
ROUND(原価 * 販売数 * 1.3) AS 金額,
IF((原価 * 販売数 * 1.3) >= 800000, 0.078, 0.062) AS 値引率,
ROUND((原価 * 販売数 * 1.3) * IF((原価 * 販売数 * 1.3) >= 800000, 0.078, 0.062)) AS 販売額,
(ROUND((原価 * 販売数 * 1.3) * IF((原価 * 販売数 * 1.3) >= 800000, 0.078, 0.062)) - 原価) * 販売数 AS 利益額
FROM `販売一覧表`
ORDER BY 販売数 DESC;
